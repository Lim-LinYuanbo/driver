#ifndef QST_QMC5883P_REGISTERS_H
#define QST_QMC5883P_REGISTERS_H

/* QMC5883P寄存器地址定义 */
#define QMC5883P_ADDR_DATA_X_LSB      0x00
#define QMC5883P_ADDR_DATA_X_MSB      0x01
#define QMC5883P_ADDR_DATA_Y_LSB      0x02
#define QMC5883P_ADDR_DATA_Y_MSB      0x03
#define QMC5883P_ADDR_DATA_Z_LSB      0x04
#define QMC5883P_ADDR_DATA_Z_MSB      0x05
#define QMC5883P_ADDR_STATUS          0x06
#define QMC5883P_ADDR_TEMP_LSB        0x07
#define QMC5883P_ADDR_TEMP_MSB        0x08
#define QMC5883P_ADDR_CONFIG1         0x09
#define QMC5883P_ADDR_CONFIG2         0x0A
#define QMC5883P_ADDR_RESET           0x0B
#define QMC5883P_ADDR_CHIP_ID         0x0D

/* 状态寄存器位定义 */
#define QMC5883P_STATUS_DRDY          (1 << 0)  /* 数据准备就绪 */
#define QMC5883P_STATUS_OVL           (1 << 1)  /* 数据溢出 */
#define QMC5883P_STATUS_DOR           (1 << 2)  /* 数据被覆盖 */

/* 配置寄存器1位定义 */
#define QMC5883P_CONFIG1_OSR_SHIFT    6
#define QMC5883P_CONFIG1_OSR_512      (0 << QMC5883P_CONFIG1_OSR_SHIFT)
#define QMC5883P_CONFIG1_OSR_256      (1 << QMC5883P_CONFIG1_OSR_SHIFT)
#define QMC5883P_CONFIG1_OSR_128      (2 << QMC5883P_CONFIG1_OSR_SHIFT)
#define QMC5883P_CONFIG1_OSR_64       (3 << QMC5883P_CONFIG1_OSR_SHIFT)

#define QMC5883P_CONFIG1_RANGE_SHIFT  4
#define QMC5883P_CONFIG1_RANGE_2G     (0 << QMC5883P_CONFIG1_RANGE_SHIFT)
#define QMC5883P_CONFIG1_RANGE_8G     (1 << QMC5883P_CONFIG1_RANGE_SHIFT)

#define QMC5883P_CONFIG1_DR_SHIFT     2
#define QMC5883P_CONFIG1_DR_10Hz      (0 << QMC5883P_CONFIG1_DR_SHIFT)
#define QMC5883P_CONFIG1_DR_50Hz      (1 << QMC5883P_CONFIG1_DR_SHIFT)
#define QMC5883P_CONFIG1_DR_100Hz     (2 << QMC5883P_CONFIG1_DR_SHIFT)
#define QMC5883P_CONFIG1_DR_200Hz     (3 << QMC5883P_CONFIG1_DR_SHIFT)

#define QMC5883P_CONFIG1_MODE_SHIFT   0
#define QMC5883P_CONFIG1_MODE_STANDBY (0 << QMC5883P_CONFIG1_MODE_SHIFT)
#define QMC5883P_CONFIG1_MODE_CONT    (1 << QMC5883P_CONFIG1_MODE_SHIFT)

/* 配置寄存器2位定义 */
#define QMC5883P_CONFIG2_INT_ENB      (1 << 0)  /* 中断使能 */
#define QMC5883P_CONFIG2_ROL_PNT      (1 << 6)  /* 指针滚动 */
#define QMC5883P_CONFIG2_SOFT_RST     (1 << 7)  /* 软件复位 */

/* 复位周期寄存器值 */
#define QMC5883P_RESET_PERIOD         0x01

/* 芯片ID值 */
#define QMC5883P_CHIP_ID              0xFF

#endif /* QST_QMC5883P_REGISTERS_H */    
